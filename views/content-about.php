              		<h1>What is an impact center?</h1>
              		<p>An impact center is a land based project that offers individual transformation, regenerative living education and strives to leave a positive local impact. An impact center could be an ecovillage, organic farm, yoga retreat center, or even a hostel, as long as they meet the criteria below.</p>
